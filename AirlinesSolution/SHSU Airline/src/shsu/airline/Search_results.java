        // TODO add your handling code here:
        
         co = (int) (Integer.parseInt(con.getText()));
         AirlineDatabase ar = new AirlineDatabase();
        try {
            ar.load();
            ar.ListFlightDetail(co);
        } catch (Exception ex) {
            Logger.getLogger(Search_results.class.getName()).log(Level.SEVERE, null, ex);
        }
        int f = 0;
        int p = 0;
        String ca,m,s,a,ac,dd,dt,da,ad,at,aa = ""; 
            
        Flight fS = new Flight();
        fS = ar.getFlight();
        Depart dS = new Depart();
        dS = ar.getDepart();
        Arrive  aS = new Arrive();
        aS = ar.getArrive();
        
        f = fS.getFnum();
        String aF = Integer.toString(f);
        disF.setText(aF);
        p = fS.getPrice();
        String aP = Integer.toString(p);
        disP.setText(aP);
        disC.setText(fS.getCab());
        disM.setText(fS.getMeal());
        disS.setText(fS.getSta());
        disA.setText(fS.getArc());
        dDate.setText(dS.getDdate());
        dTime.setText(dS.getDtime());
        dAirport.setText(dS.getAp());
        aDate.setText(aS.getAdate());
        aTime.setText(aS.getAtime());
        aAirport.setText(aS.getAp());       
        
        
        
         
